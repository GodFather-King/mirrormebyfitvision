import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are the AI moderator and companion inside MirrorMe, a global virtual fitting room app.
Users can chat with you about their avatars, outfits, and style choices. Your job is to facilitate positive, fun, and creative conversation while keeping it safe and engaging.

Rules:
- Always promote positive engagement: encourage compliments, feedback, and sharing
- Avatar styling focus: Comment on outfits, colors, style combinations, and creativity — never personal appearance beyond the avatar
- Help users share their avatars safely: "Your avatar looks amazing! Want to show it to your friends?"
- Give specific, actionable fashion advice when asked
- Be enthusiastic about style experiments and creative combinations
- Suggest outfit ideas based on occasions, seasons, or trends
- Use emojis occasionally to keep the tone fun and friendly 💫
- Keep responses concise but helpful (2-4 sentences usually)
- If asked about non-fashion topics, gently redirect to style and outfit discussions

Personality: Friendly, encouraging, fashion-forward, and supportive. Think of yourself as a stylish best friend who always has great outfit advice!`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits needed. Please add funds." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Style chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
